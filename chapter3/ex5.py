'''
비교연산자 - 두 수를 비교할 때 사용
결과 -> bool(논리값)
bool 타입의 데이터 -> True, False
비교연산자는 true와 false 이 두가지 값밖에 없음
비교연산자의 기준은 왼쪽!
'''

vallue1 = 10
value2 = 5
eqresult = vallue1 == value2
neqresult = vallue1 != value2
gtresult = vallue1 > value2
ltresult = vallue1 < value2
gteresult = vallue1 >= value2
lteresult = vallue1 <= value2

print("eqresult = ", eqresult)
print("neresult = ", neqresult)
print("gtresult = ", gtresult)
print("ltresult = ", ltresult)
print("gtresult = ", gteresult)
print("ltresult = ", lteresult)

